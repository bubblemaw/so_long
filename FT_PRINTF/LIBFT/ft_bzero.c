#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((unsigned char *)s)[i] = '\0';
		i++;
	}
}

/*
La fonction bzero() met à 0 (octets contenant « \0 ») les n premiers octets
du bloc pointé par s.
*/